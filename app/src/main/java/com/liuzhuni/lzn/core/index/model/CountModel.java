public class CountModel implements Serializable{
    private int shoplists;
    private int pushs;


    public CountModel(int shoplists, int pushs) {
        this.shoplists = shoplists;
        this.pushs = pushs;
    }

    public int getShoplists() {
        return shoplists;
    }

    public void setShoplists(int shoplists) {
        this.shoplists = shoplists;
    }

    public int getPushs() {
        return pushs;
    }

    public void setPushs(int pushs) {
        this.pushs = pushs;
    }
}
