public class BuyListModel implements Serializable{

    private int id;
    private int status;
    private String value;

    public BuyListModel(int id, int status, String value) {
        this.id = id;
        this.status = status;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
